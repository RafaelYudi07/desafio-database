-- Lista de cadastros com tags
SELECT 
    c.nome AS nome,
    c.estado AS estado,
    c.cidade AS cidade,
    GROUP_CONCAT(t.titulo ORDER BY t.titulo SEPARATOR ', ') AS tags
FROM 
    cadastros c
LEFT JOIN 
    cadastros_tags ct ON c.id = ct.cadastro_id
LEFT JOIN 
    tags t ON ct.tag_id = t.id
GROUP BY 
    c.id, c.nome, c.estado, c.cidade;

-- Lista de receitas liquidadas em um determinado período
SELECT 
    c.nome, 
    c.documento, 
    l.descricao, 
    l.liquidacao, 
    l.valor_liquidado
FROM 
    lancamentos l
LEFT JOIN cadastros c ON l.cadastro_id = c.id
WHERE 
    l.tipo = 'receber' 
    AND l.status = 'liquidado'
    AND l.liquidacao BETWEEN '2024-01-01' AND '2024-12-31';

-- Total de receitas liquidadas, por categoria mensal
SELECT 
    DATE_FORMAT(l.vencimento, '%Y-%m') AS mes_ano,
    c.titulo AS categoria,                        
    SUM(l.valor) AS total_receita               
FROM 
    lancamentos l
JOIN 
    categorias c ON l.categoria_id = c.id
WHERE 
    l.tipo = 'receber' AND                        
    l.status = 'liquidado'                        
GROUP BY 
    DATE_FORMAT(l.vencimento, '%Y-%m'),           
    c.titulo
ORDER BY 
    mes_ano desc;

   
-- Lista de despesas em aberto
SELECT 
    c.nome, 
    c.documento, 
    l.descricao, 
    l.vencimento, 
    l.valor
FROM 
    lancamentos l
LEFT JOIN cadastros c ON l.cadastro_id = c.id
WHERE 
    l.tipo = 'pagar' 
    AND l.status = 'aberto';
    
-- Total de despesas liquidadas, por categoria mensal:
SELECT 
    DATE_FORMAT(l.vencimento, '%Y-%m') AS mes_ano, 
    c.titulo AS categoria,                       
    SUM(l.valor) AS total_despesa                
FROM 
    lancamentos l
JOIN 
    categorias c ON l.categoria_id = c.id
WHERE 
    l.tipo = 'pagar' AND                       
    l.status = 'liquidado'                      
GROUP BY 
    DATE_FORMAT(l.vencimento, '%Y-%m'),          
    c.titulo
ORDER BY 
    mes_ano;