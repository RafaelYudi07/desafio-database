CREATE TABLE lancamentos(
id INT NOT NULL AUTO_INCREMENT,
PRIMARY KEY (id),
tipo VARCHAR (10) NOT NULL,
CHECK(tipo IN ('pagar','receber')),
status VARCHAR (10) NOT NULL,
CHECK (status IN ('aberto','liquidado')),
descricao VARCHAR (255),
valor DECIMAL (10,2) NOT NULL,
valor_liquidado DECIMAL (10,2),
vencimento DATE,
liquidacao DATE,
cadastro_id INT,
categoria_id INT,
CONSTRAINT FK_cadastro_id
    FOREIGN KEY (cadastro_id) REFERENCES cadastros (id) ON DELETE NO ACTION ON UPDATE NO ACTION,
CONSTRAINT FK_categoria_id
    FOREIGN KEY (categoria_id) REFERENCES categorias (id) ON DELETE NO ACTION ON UPDATE NO ACTION
);

